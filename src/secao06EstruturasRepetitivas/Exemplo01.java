package secao06EstruturasRepetitivas;

public class Exemplo01 {

	public static void main(String[] args) {
		
		for (int i = 0; i < 5; i++) {
			System.out.println("Valor de i " + i);
		}
		
		System.out.printf("%n");
		
		for (int i = 4; i >= 0; i--) {
			System.out.println("Valor de i " + i);
		}

	}

}